/**
 * WikiPage (wiki/page.tsx) - Главная страница вики (базы знаний)
 * 
 * Назначение:
 * - Отображение списка статей вики
 * - Фильтрация статей по тегам и поисковому запросу
 * - Группировка статей по категориям
 * - Навигация к детальным страницам статей
 * 
 * Функциональность:
 * - WikiHeader: заголовок с кнопкой создания новой статьи
 * - WikiFilters: фильтры по тегам и поиск по тексту
 * - WikiCategory: отображение категорий со статьями
 * - Фильтрация статей по выбранному тегу и поисковому запросу
 * - Группировка отфильтрованных статей по категориям
 * - Сортировка категорий (категория "Архитектура" всегда первая)
 * 
 * Связи:
 * - AppLayout: оборачивает страницу в общий layout
 * - WikiHeader: управляет созданием новых статей
 * - WikiFilters: управляет фильтрацией и поиском
 * - WikiCategory: отображает статьи в категориях
 * - Детальные страницы статей: /wiki/[id]
 * 
 * Поведение:
 * - При загрузке отображает все статьи, сгруппированные по категориям
 * - При выборе тега фильтрует статьи, содержащие этот тег
 * - При вводе поискового запроса фильтрует по названию и описанию
 * - Категории сортируются: "Архитектура" первая, остальные по алфавиту
 * - При клике на статью происходит переход на детальную страницу
 */

'use client'

import AppLayout from "@/components/AppLayout"
import WikiHeader from "@/components/wiki/WikiHeader"
import WikiFilters from "@/components/wiki/WikiFilters"
import WikiCategory from "@/components/wiki/WikiCategory"
import { Box, Flex } from "@radix-ui/themes"
import { useState } from "react"
import styles from './wiki.module.css'

/**
 * WikiPage - интерфейс статьи вики
 * 
 * Структура:
 * - id: уникальный идентификатор статьи
 * - title: название статьи
 * - description: краткое описание статьи
 * - tags: массив тегов статьи (например, ['#настройка', '#финансы'])
 * - date: дата создания или последнего обновления
 * - category: категория статьи (например, 'Архитектура', 'Настройка')
 */
interface WikiPage {
  id: string
  title: string
  description: string
  tags: string[]
  date: string
  category: string
}

/**
 * WikiPage - компонент главной страницы вики
 * 
 * Состояние:
 * - selectedTag: выбранный тег для фильтрации (null - все теги)
 * - searchQuery: поисковый запрос для фильтрации статей
 */
export default function WikiPage() {
  // Выбранный тег для фильтрации: null - все теги, иначе - конкретный тег
  const [selectedTag, setSelectedTag] = useState<string | null>(null)
  // Поисковый запрос для фильтрации статей по названию и описанию
  const [searchQuery, setSearchQuery] = useState('')

  /**
   * wikiPages - моковые данные страниц вики
   * 
   * Структура каждой статьи:
   * - id: уникальный идентификатор
   * - title: название статьи
   * - description: краткое описание
   * - tags: массив тегов (с префиксом #)
   * - date: дата в формате DD.MM.YYYY
   * - category: категория статьи
   * 
   * TODO: Заменить на реальные данные из API
   */
  const wikiPages: WikiPage[] = [
    {
      id: '1',
      title: 'Архитектура продукта',
      description: 'Общая архитектура и компоненты системы HR Helper',
      tags: ['#architect'],
      date: '04.11.2025',
      category: 'Архитектура',
    },
    {
      id: '2',
      title: 'Первичная настройка компании',
      description: 'Пошаговая инструкция по первоначальной настройке компании в системе',
      tags: ['#настройка', '#финансы'],
      date: '03.11.2025',
      category: 'Настройка',
    },
    {
      id: '3',
      title: 'Настройка интервьюеров',
      description: 'Как добавить и настроить интервьюеров в системе',
      tags: ['#интервьюеры', '#настройка'],
      date: '02.11.2025',
      category: 'Настройка',
    },
    {
      id: '4',
      title: 'Управление пользователями',
      description: 'Добавление, редактирование и управление пользователями системы',
      tags: ['#пользователи', '#настройка'],
      date: '01.11.2025',
      category: 'Настройка',
    },
  ]

  /**
   * allTags - массив всех доступных тегов для фильтрации
   * 
   * Структура тега:
   * - id: уникальный идентификатор тега (без префикса #)
   * - label: отображаемое название тега (с префиксом #)
   * - color: цвет тега для визуального отображения
   * 
   * Используется для:
   * - Отображения списка тегов в WikiFilters
   * - Фильтрации статей по выбранному тегу
   * - Визуального выделения тегов разными цветами
   */
  const allTags = [
    { id: 'ai', label: '#ai', color: '#ef4444' },
    { id: 'architect', label: '#architect', color: '#ef4444' },
    { id: 'вакансии', label: '#вакансии', color: '#ef4444' },
    { id: 'интеграции', label: '#интеграции', color: '#84cc16' },
    { id: 'интервьюеры', label: '#интервьюеры', color: '#10b981' },
    { id: 'использование', label: '#использование', color: '#10b981' },
    { id: 'календарь', label: '#календарь', color: '#a855f7' },
    { id: 'метрики', label: '#метрики', color: '#f59e0b' },
    { id: 'настройка', label: '#настройка', color: '#3b82f6' },
    { id: 'пользователи', label: '#пользователи', color: '#6b7280' },
    { id: 'финансы', label: '#финансы', color: '#06b6d4' },
  ]
  
  /**
   * filteredPages - отфильтрованный список страниц вики
   * 
   * Логика фильтрации:
   * 1. Фильтр по тегу: если selectedTag не null, проверяет наличие тега в статье (без учета регистра)
   * 2. Фильтр по поиску: если searchQuery не пустой, проверяет наличие в названии или описании (без учета регистра)
   * 3. Оба условия должны выполняться одновременно (логическое И)
   * 
   * Поведение:
   * - Если selectedTag === null - пропускает все статьи (не фильтрует по тегу)
   * - Если searchQuery пустой - пропускает все статьи (не фильтрует по поиску)
   * - Сравнение выполняется без учета регистра (toLowerCase)
   * 
   * Результат используется для:
   * - Группировки по категориям
   * - Отображения в WikiCategory компонентах
   */
  const filteredPages = wikiPages.filter(page => {
    // Фильтр по тегу: проверяет наличие выбранного тега в массиве тегов статьи
    const matchesTag = !selectedTag || page.tags.some(tag => tag.toLowerCase() === selectedTag.toLowerCase())
    // Фильтр по поиску: проверяет наличие поискового запроса в названии или описании
    const matchesSearch = !searchQuery || 
      page.title.toLowerCase().includes(searchQuery.toLowerCase()) ||
      page.description.toLowerCase().includes(searchQuery.toLowerCase())
    // Оба условия должны выполняться
    return matchesTag && matchesSearch
  })

  /**
   * categories - массив уникальных категорий из отфильтрованных страниц
   * 
   * Функциональность:
   * - Извлекает все уникальные категории из отфильтрованных страниц
   * - Используется для группировки статей по категориям
   * 
   * Поведение:
   * - Создает Set для получения уникальных значений
   * - Преобразует Set в массив
   */
  const categories = Array.from(new Set(filteredPages.map(page => page.category)))
  
  /**
   * sortedCategories - отсортированный массив категорий
   * 
   * Логика сортировки:
   * 1. Категория "Архитектура" всегда первая (приоритет)
   * 2. Остальные категории сортируются по алфавиту (localeCompare)
   * 
   * Поведение:
   * - Используется для определения порядка отображения категорий
   * - Обеспечивает, что категория "Архитектура" всегда видна первой
   */
  const sortedCategories = [...categories].sort((a, b) => {
    if (a === 'Архитектура') return -1 // "Архитектура" всегда первая
    if (b === 'Архитектура') return 1
    return a.localeCompare(b) // Остальные по алфавиту
  })

  /**
   * groupedPages - объект сгруппированных страниц по категориям
   * 
   * Структура:
   * - Ключ: название категории (например, 'Архитектура', 'Настройка')
   * - Значение: массив страниц этой категории
   * 
   * Функциональность:
   * - Группирует отфильтрованные страницы по категориям
   * - Использует отсортированный список категорий для порядка
   * - Включает только категории, в которых есть хотя бы одна страница после фильтрации
   * 
   * Поведение:
   * - Создает объект, где ключ - категория, значение - массив страниц
   * - Используется для рендеринга WikiCategory компонентов
   * - Порядок категорий соответствует sortedCategories
   */
  const groupedPages = sortedCategories.reduce((acc, category) => {
    // Фильтруем страницы по текущей категории
    const pages = filteredPages.filter(page => page.category === category)
    // Добавляем категорию только если в ней есть страницы
    if (pages.length > 0) {
      acc[category] = pages
    }
    return acc
  }, {} as Record<string, WikiPage[]>)

  /**
   * Рендер компонента страницы вики
   * 
   * Структура:
   * - AppLayout: оборачивает страницу в общий layout
   * - WikiHeader: заголовок с кнопкой создания статьи
   * - WikiFilters: фильтры по тегам и поиск
   * - WikiContent: контент со статьями, сгруппированными по категориям
   */
  return (
    <AppLayout pageTitle="Wiki">
      <Box className={styles.wikiContainer}>
        {/* Заголовок страницы вики
            - Содержит название раздела
            - Кнопка создания новой статьи
            - Дополнительные действия */}
        <WikiHeader />
        
        {/* Фильтры и поиск
            - tags: массив всех доступных тегов для отображения
            - selectedTag: текущий выбранный тег (null - все теги)
            - onTagSelect: обработчик выбора тега (устанавливает selectedTag)
            - searchQuery: текущий поисковый запрос
            - onSearchChange: обработчик изменения поискового запроса (устанавливает searchQuery) */}
        <WikiFilters
          tags={allTags}
          selectedTag={selectedTag}
          onTagSelect={setSelectedTag}
          searchQuery={searchQuery}
          onSearchChange={setSearchQuery}
        />
        
        {/* Контент со статьями, сгруппированными по категориям
            - Object.entries(groupedPages): преобразует объект в массив [категория, страницы]
            - Для каждой категории рендерится WikiCategory компонент
            - Порядок категорий соответствует sortedCategories (Архитектура первая) */}
        <Box className={styles.wikiContent}>
          {Object.entries(groupedPages).map(([category, pages]) => (
            <WikiCategory
              key={category}
              category={category}
              pages={pages}
            />
          ))}
        </Box>
      </Box>
    </AppLayout>
  )
}
