/**
 * WikiEditPage (wiki/[id]/edit/page.tsx) - Страница редактирования статьи вики
 * 
 * Назначение:
 * - Редактирование существующей статьи вики
 * - Создание новой статьи вики (если id === 'new')
 * - Управление содержимым, категориями, тегами статьи
 * 
 * Функциональность:
 * - WikiEditForm: компонент формы редактирования статьи
 *   - Редактирование заголовка и содержимого
 *   - Выбор категории и приложения
 *   - Управление тегами
 *   - Настройка публикации (опубликовано/черновик)
 *   - Сохранение изменений
 * 
 * Связи:
 * - AppLayout: оборачивает страницу в общий layout
 * - WikiEditForm: компонент с формой редактирования статьи
 * - useParams: получение ID статьи из URL
 * - wiki/page.tsx: страница списка статей, откуда происходит переход на редактирование
 * 
 * Поведение:
 * - Если id === 'new' - создается новая статья (isNew = true)
 * - Если id !== 'new' - редактируется существующая статья (isNew = false)
 * - При загрузке загружает данные статьи (в текущей реализации использует моковые данные)
 * - При сохранении обновляет статью или создает новую
 * 
 * TODO: Заменить моковые данные на реальные из API
 */

'use client'

import AppLayout from "@/components/AppLayout"
import WikiEditForm from "@/components/wiki/WikiEditForm"
import { Box } from "@radix-ui/themes"
import { useParams } from "next/navigation"

/**
 * WikiEditPage - компонент страницы редактирования статьи вики
 * 
 * Функциональность:
 * - Определяет, создается ли новая статья или редактируется существующая
 * - Загружает данные статьи для редактирования (если не новая)
 * - Рендерит форму редактирования через WikiEditForm
 */
export default function WikiEditPage() {
  // Получение динамических параметров из URL
  const params = useParams()
  // ID статьи из URL параметра [id]
  const pageId = params.id as string
  // Флаг создания новой статьи: true если id === 'new', иначе false
  const isNew = pageId === 'new'

  /**
   * wikiPage - данные статьи для редактирования
   * 
   * Структура:
   * - id: уникальный идентификатор статьи
   * - title: заголовок статьи
   * - slug: URL-дружественный идентификатор
   * - category: категория статьи
   * - relatedApp: связанное приложение (опционально)
   * - tags: массив тегов
   * - description: краткое описание
   * - content: содержимое статьи (Markdown)
   * - order: порядок сортировки
   * - isPublished: флаг публикации (true - опубликовано, false - черновик)
   * 
   * TODO: Заменить моковые данные на реальные из API
   */
  const wikiPage = isNew ? undefined : {
    id: pageId,
    title: 'Архитектура продукта HR Helper',
    slug: 'arhitektura-produkta-hr-helper',
    category: 'architect',
    relatedApp: '',
    tags: ['Архитектура', '#architect'],
    description: 'Описание архитектуры продукта HR Helper',
    content: `# Архитектура продукта HR Helper

## Основные модули

1. Управление пользователями и аутентификация (apps.accounts)
   - Регистрация и авторизация пользователей
   - Интеграция с Google OAuth
   - Управление профилями и правами доступа
   - Интеграции с внешними системами

2. Финансы и грейды (apps.finance)
   - Управление грейдами компании
   - Расчет зарплатных вилок
   - Налоговые настройки и курсы валют
   - Бенчмарки зарплат

...`,
    order: 0,
    isPublished: true,
  }

  /**
   * Рендер компонента страницы редактирования статьи
   * 
   * Структура:
   * - AppLayout: оборачивает страницу в общий layout
   * - WikiEditForm: компонент формы редактирования
   *   - initialData: данные статьи для редактирования (undefined для новой статьи)
   *   - isNew: флаг создания новой статьи
   */
  return (
    <AppLayout pageTitle="Wiki">
      <Box>
        {/* Компонент формы редактирования статьи
            - initialData: данные статьи для редактирования (undefined если создается новая)
            - isNew: флаг создания новой статьи (true для /wiki/new/edit, false для /wiki/[id]/edit)
            - При сохранении создает новую статью или обновляет существующую */}
        <WikiEditForm 
          initialData={wikiPage}
          isNew={isNew}
        />
      </Box>
    </AppLayout>
  )
}
