/**
 * VacancyEditRedirect (vacancies/[id]/edit/page.tsx) - Редирект на страницу вакансий с редактированием
 * 
 * Назначение:
 * - Перенаправление с URL /vacancies/[id]/edit на /vacancies?edit=[id]
 * - Обеспечивает совместимость со старыми ссылками на редактирование
 * - Автоматически открывает модальное окно редактирования вакансии
 * 
 * Функциональность:
 * - Читает ID вакансии из URL параметра [id]
 * - Выполняет редирект на /vacancies?edit=[id]
 * - Если ID отсутствует - редирект на /vacancies
 * 
 * Связи:
 * - useParams: получение динамического параметра [id] из URL
 * - useRouter: выполнение редиректа
 * - vacancies/page.tsx: страница вакансий, которая обрабатывает параметр ?edit=
 * 
 * Поведение:
 * - При загрузке компонента сразу выполняет редирект
 * - Использует router.replace вместо router.push (не добавляет запись в историю)
 * - Возвращает null (компонент не рендерит никакого контента)
 * 
 * Причина существования:
 * - Обеспечивает обратную совместимость со старыми ссылками на редактирование вакансии
 * - Упрощает навигацию: вместо отдельной страницы редактирования используется модальное окно на странице списка
 */

'use client'

import { useRouter, useParams } from "next/navigation"
import { useEffect } from "react"

/**
 * VacancyEditRedirect - компонент редиректа на редактирование вакансии
 * 
 * Функциональность:
 * - Выполняет редирект на страницу вакансий с параметром ?edit=
 * - Не рендерит никакого контента (возвращает null)
 */
export default function VacancyEditRedirect() {
  // Хук Next.js для программной навигации
  const router = useRouter()
  // Получение динамических параметров из URL
  const params = useParams()
  // ID вакансии из URL параметра [id]
  const id = params?.id as string

  /**
   * useEffect - выполнение редиректа при загрузке компонента
   * 
   * Функциональность:
   * - Если ID вакансии существует - редирект на /vacancies?edit=[id]
   * - Если ID отсутствует - редирект на /vacancies
   * 
   * Поведение:
   * - Выполняется один раз при монтировании компонента
   * - Использует router.replace (не добавляет запись в историю браузера)
   * - Кодирует ID через encodeURIComponent для безопасной передачи в URL
   * 
   * Связи:
   * - vacancies/page.tsx: обрабатывает параметр ?edit= и открывает модальное окно редактирования
   */
  useEffect(() => {
    if (id) {
      // Редирект на страницу вакансий с параметром редактирования
      router.replace(`/vacancies?edit=${encodeURIComponent(id)}`)
    } else {
      // Если ID отсутствует - редирект на список вакансий
      router.replace('/vacancies')
    }
  }, [router, id]) // Зависимости: router и id

  // Компонент не рендерит никакого контента
  return null
}
